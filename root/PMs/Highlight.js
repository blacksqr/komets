function Highlight (id, tag, r, g, b, a) {
 var h_id = 'Highlight_for__' + id;
 $('body').append('<div id="' + h_id + '" class="' + tag + '"></div>');
 
 var Enlight_tx = $('#' + id).width();
 var Enlight_ty = $('#' + id).height();
 var Enlight_x  = $('#' + id).offset().left;
 var Enlight_y  = $('#' + id).offset().top;

 $('#' + h_id).css({'background-color': 'rgba(' + r + ',' + g + ',' + b + ',' + a + ')', 'position' : 'absolute', 'top' : Enlight_y + 'px', 'left' : Enlight_x + 'px', 'width' : Enlight_tx + 'px', 'height' : Enlight_ty + 'px'});
}

//___________________________________________________________________________________________________________________________________________
function Kill_highlights_of_class (c) {
 $('div.' + c).remove();
}

